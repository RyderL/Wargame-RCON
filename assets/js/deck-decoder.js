function DeckDecoder(val) {
  this.side = "NONE";
  this.nation = "NONE";
  this.spec = 7;
  this.era = 2;
  this.binary = "";
  this.deck = val;
  this.units = [[],[],[],[],[],[],[],[],[]];
  this.unitsCount = [];
  this.done = false;

  try {
    this.decode();
  } catch (e) {
    console.log(`unable to parse deck, deck: ${this.deck}`);
  }
}

DeckDecoder.prototype.decode = function() {
  if (this.deck.length < 4 ) { return; }
  
  let binaryData = null;
  
  try {
    binaryData = Buffer.from(data, 'base64').toString('binary');
  } catch {
    binaryData = atob(this.deck.substring(1));
  }

  for(let i=0; i<binaryData.length; i++) {
    let bin = binaryData.charCodeAt(i).toString(2);
    this.binary += Array(8-bin.length+1).join("0") + bin;
  }

  this.code = this.binary.substring(0, 12);
  this.side = this.binary.charAt(1) * 1;
  this.nation = NATION_MAPPING[this.code];
  this.spec = this.getInt(12, 15);
  this.era = this.getInt(15, 17);

  let pos = 26;

  [this.getInt(17, 21), this.getInt(21, 26)].forEach((val, idx) => pos = this.decodeUnits(pos, idx, val));

  this.decodeOtherUnits(pos);

  this.unitsCount = this.units.map(x => x.reduce((v1, v2) => v1 + v2.quantity, 0));

  if(!this.nation) {
    console.log(`unable to parse deck, code: ${this.code}, deck: ${this.deck}`);
    return;
  }
  
  this.done = true;
}

DeckDecoder.prototype.decodeUnits = function(pos, idx, count) {
  if(!count) {
    return pos;
  }

  for(let i = 0; i < count; i++) {
    let exp = this.getInt(pos, pos + 3);
    let unit = UNITS_MAPPING[this.side][this.getInt(pos + 3, pos + 14)];
    let transport = UNITS_MAPPING[this.side][this.getInt(pos + 14, pos + 25)];


    let craft = null;

    if(idx == 0) {
      craft = UNITS_MAPPING[this.side][this.getInt(pos + 25, pos + 36)];
      pos += 36;
    } else {
      if((transport.side == 0 && transport.id == 573) || transport.side == 1 && transport.id == 458) {
        craft = transport;
        transport = null;
      }
  
      pos += 25;
    }

    this.push(exp, unit, transport, craft);
  }

  return pos;
}

DeckDecoder.prototype.decodeOtherUnits = function(pos) {
  while(this.binary.length - pos > 12) {
    let exp = this.getInt(pos, pos + 3);
    let unit = UNITS_MAPPING[this.side][this.getInt(pos + 3, pos + 14)];

    this.push(exp, unit);

    pos += 14;
  }
}

DeckDecoder.prototype.getInt = function(begin, end) {
  return parseInt(this.binary.substring(begin, end), 2);
}

DeckDecoder.prototype.push = function(exp, unit, transport, craft) {
  let _exp = SPEC_EFFECT[this.spec].indexOf(unit.type) > -1 ? exp + SPEC_EXP[this.spec] : exp;
  
  if(_exp > 4) {
    _exp = 4;
  }

  let quantity = unit.available[_exp];
  let type = craft ? 8 : unit.type;

  if(transport && transport.available[_exp] < quantity) {
    quantity = transport.available[_exp];
  }
  
  let cost = unit.cost;
  
  if(transport) {
    cost += "+" + transport.cost;
  }

  if(craft) {
    cost += "+" + craft.cost;
  }

  if(AVALIABILITY_UNITS_MAPPING[this.nation]) {
    quantity = Math.round(AVALIABILITY_UNITS_MAPPING[this.nation][1] * quantity);
  }

  this.units[type].push({exp, unit, transport, craft, quantity, cost});
}

